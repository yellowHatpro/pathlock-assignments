package data;

public class User {

    private String email;
    private Long id;
    private String name;
    private String phone;
    private String username;
    private String website;

    public String getEmail() {
        return email;
    }
    public Long getId() {
        return id;
    }
    public String getName() {
        return name;
    }
    public String getPhone() {
        return phone;
    }
    public String getUsername() {
        return username;
    }
    public String getWebsite() {
        return website;
    }
}
